    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // pegar os dados informados:
        String usuario = campoUsuario.getText();
        String senha = String.valueOf(campoSenha.getPassword());
        String nivelAcesso = String.valueOf(campoNivelAcesso.getSelectedItem());

        // Preparando para conectar
        try {
            // Criando um objeto do tipo usuario
            Usuario u = new Usuario(usuario, senha, nivelAcesso);

            // Criando um usuarioDAO para manusear os dados
            UsuarioDAO uDAO = new UsuarioDAO();

            // Cadastrando no banco:
            uDAO.cadastrarUsuario(u);
            JOptionPane.showMessageDialog(this, "Usuario cadastrado com sucesso");

            // fechar a janela ap√≥s o cadastro:
            this.dispose();

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar usuario");
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

