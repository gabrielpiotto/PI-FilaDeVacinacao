    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // Traduzindo os botões do JOP:
        UIManager.put("OptionPane.yesButtonText", "Sim");
        UIManager.put("OptionPane.noButtonText", "Não");
        UIManager.put("OptionPane.cancelButtonText", "Cancelar");

        try {
            // Recebendo os dados:
            String usuario = txtUsuario.getText();
            String senha = txtSenha.getText();
            String nivelAcesso = txtNivelAcesso.getText();

            // Não cadastrar ficha em branco:
            if (usuario.length() == 0 || senha.length() == 0 || nivelAcesso.length() == 0) {
                JOptionPane.showMessageDialog(this, "Preencha os campos corretamente.");
                return;
            }

            if (nivelAcesso.equals("Administrador") || nivelAcesso.equals("Atendente")) {

            } else {
                JOptionPane.showMessageDialog(this, "Nivel de Acesso Invalido");
                return;
            }

            Usuario u = new Usuario(usuario, senha, nivelAcesso);
            UsuarioDAO uDAO = new UsuarioDAO();

            // Confirmação de Cadastro:
            int escolha = JOptionPane.showConfirmDialog(this, "Deseja mesmo cadastrar o usuario " + txtUsuario.getText() + " ?");
            if (escolha == JOptionPane.YES_OPTION) {
                JOptionPane.showMessageDialog(this, "Usuario Cadastrado com Sucesso!");
                uDAO.cadastrarUsuario(u);

                // Limpar os campos e Atualizando tabela:
                txtUsuario.setText("");
                txtSenha.setText("");
                txtNivelAcesso.setText("");

                readTabela();
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Problemas tecnicos, tente mais tarde");
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        // Traduzindo os botões do JOP:
        UIManager.put("OptionPane.yesButtonText", "Sim");
        UIManager.put("OptionPane.noButtonText", "Não");
        UIManager.put("OptionPane.cancelButtonText", "Cancelar");

        // Confirmação de atualização:
        try {
            int escolha = JOptionPane.showConfirmDialog(this, "Deseja mesmo atualizar o usuario " + txtUsuario.getText() + " ?");
            if (escolha == JOptionPane.YES_OPTION) {
                if (tabelaUsuarios.getSelectedRow() != -1) {
                    // Recebendo os dados:
                    int id = (int) tabelaUsuarios.getValueAt(tabelaUsuarios.getSelectedRow(), 0);
                    String usuario = txtUsuario.getText();
                    String senha = txtSenha.getText();
                    String nivelAcesso = txtNivelAcesso.getText();

                    // Não cadastrar ficha em branco:
                    if (usuario.length() == 0 || senha.length() == 0 || nivelAcesso.length() == 0) {
                        JOptionPane.showMessageDialog(this, "Preencha os campos corretamente.");
                        return;
                    }

                    // Aceitar somente dois niveis de acesso:
                    if (nivelAcesso.equals("Administrador") || nivelAcesso.equals("Atendente")) {

                    } else {
                        JOptionPane.showMessageDialog(this, "Nivel de Acesso Invalido");
                        return;
                    }

                    Usuario u = new Usuario(id, usuario, senha, nivelAcesso);
                    UsuarioDAO uDAO = new UsuarioDAO();
                    uDAO.atualizarUsuario(u);

                    // Limpar os campos e Atualizando tabela:
                    txtUsuario.setText("");
                    txtSenha.setText("");
                    txtNivelAcesso.setText("");

                    readTabela();
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Problemas tecnicos, tente mais tarde");
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void tabelaUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaUsuariosMouseClicked
        // Pegando os dados das linhas selecionadas:
        if (tabelaUsuarios.getSelectedRow() != -1) {
            txtUsuario.setText(tabelaUsuarios.getValueAt(tabelaUsuarios.getSelectedRow(), 1).toString());
            txtSenha.setText(tabelaUsuarios.getValueAt(tabelaUsuarios.getSelectedRow(), 2).toString());
            txtNivelAcesso.setText(tabelaUsuarios.getValueAt(tabelaUsuarios.getSelectedRow(), 3).toString());
        }
    }//GEN-LAST:event_tabelaUsuariosMouseClicked

