    public void readTabela() {
        DefaultTableModel modelo = (DefaultTableModel) tabelaUsuarios.getModel();
        modelo.setNumRows(0);
        UsuarioDAO uDAO = new UsuarioDAO();

        try {
            for (Usuario u : uDAO.read()) {
                modelo.addRow(new Object[]{
                    u.getId(),
                    u.getUsuario(),
                    u.getSenha(),
                    u.getNivelAcesso(),});
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Problemas tecnicos, tente mais tarde");
        }

    }

